def upper_caser(word):
    """taken word variable and changed case for upper"""
    upper_word = str(word).upper()
    return upper_word

def title_caser(word):
    """word for title case"""
    title_word = str(word).title()
    return title_word

def lower_caser(word):
    """word for lower case"""
    lower_word = str(word).title()
    return lower_word